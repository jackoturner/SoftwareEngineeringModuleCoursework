SET FOREIGN_KEY_CHECKS=0;

DROP TABLE IF EXISTS reviews;
DROP TABLE IF EXISTS pub_beers;
DROP TABLE IF EXISTS beers;
DROP TABLE IF EXISTS pubs;
DROP TABLE IF EXISTS users;

SET FOREIGN_KEY_CHECKS=1;

-- USERS
CREATE TABLE users (
  id INT NOT NULL AUTO_INCREMENT,
  first_name VARCHAR(100) NOT NULL,
  last_name VARCHAR(100) NOT NULL,
  email VARCHAR(255) NOT NULL UNIQUE,
  password_hash VARCHAR(255) NOT NULL,
  date_of_birth DATE NOT NULL,
  bio TEXT,
  profile_image VARCHAR(255),
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (id)
);

-- PUBS
CREATE TABLE pubs (
  id INT NOT NULL AUTO_INCREMENT,
  name VARCHAR(255) NOT NULL,
  address VARCHAR(255) NOT NULL,
  postcode VARCHAR(20) NOT NULL,
  latitude DECIMAL(10,8) NOT NULL,
  longitude DECIMAL(11,8) NOT NULL,
  description TEXT,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (id)
);

-- BEERS
CREATE TABLE beers (
  id INT NOT NULL AUTO_INCREMENT,
  name VARCHAR(255) NOT NULL,
  brewery VARCHAR(255),
  type VARCHAR(100),
  abv DECIMAL(3,1),
  PRIMARY KEY (id)
);

-- PUB_BEERS
CREATE TABLE pub_beers (
  pub_id INT NOT NULL,
  beer_id INT NOT NULL,
  is_available TINYINT(1) DEFAULT 1,
  PRIMARY KEY (pub_id, beer_id),
  FOREIGN KEY (pub_id) REFERENCES pubs(id) ON DELETE CASCADE,
  FOREIGN KEY (beer_id) REFERENCES beers(id) ON DELETE CASCADE
);

-- REVIEWS
CREATE TABLE reviews (
  id INT NOT NULL AUTO_INCREMENT,
  user_id INT NOT NULL,
  pub_id INT NOT NULL,
  beer_id INT NOT NULL,
  rating INT NOT NULL CHECK (rating BETWEEN 1 AND 5),
  ai_pour_score DECIMAL(3,2),
  image_url VARCHAR(255),
  comment TEXT,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (id),
  FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
  FOREIGN KEY (pub_id) REFERENCES pubs(id) ON DELETE CASCADE,
  FOREIGN KEY (beer_id) REFERENCES beers(id) ON DELETE CASCADE
);

